function frog (){

//frog body
  push();
  stroke('black');
  strokeWeight(4);
  fill('rgb(19, 66, 17)');
  ellipse(65 ,240, 50, 50);
  ellipse(50 ,225, 50, 50);
  ellipse(75,250,25,25);
  pop();

//frog eyes outer
  push();
  stroke('black');
  strokeWeight(2);
  fill('white');
  ellipse(37,200,20,20);
  ellipse(58,200,20,20);
  pop();
//frog eyes inner
  push();
  stroke('black');
  strokeWeight(2);
  fill('black');
  ellipse(37,200,10,10);
  ellipse(58,200,10,10);
  pop();
// frog mouth
  push();
  arc(50, 238, 15, 15, 0, PI + QUARTER_PI, PIE);
  pop();

}

